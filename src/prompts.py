# ========== 约束条件解析 ==========

CONSTRAINT_EXAMPLES = {
    "platform": "目标平台：PC / 主机 / 移动端 / 网页",
    "scope": "项目规模：独立小品 / AA 级 / 3A 级",
    "audience": "目标受众：核心玩家 / 休闲玩家 / 大众市场",
    "budget": "开发预算：低（1-3 人）/ 中（小团队）/ 高（工作室）",
    "timeline": "开发周期：3 个月 / 1 年 / 3 年+",
}

CONSTRAINT_TEMPLATES = {
    "platform": lambda v: f"目标平台：{v}（设计时要考虑平台特性）",
    "scope": lambda v: f"项目规模：{v}（控制内容量和复杂度）",
    "audience": lambda v: f"目标受众：{v}（调整难度和系统复杂度）",
    "budget": lambda v: f"开发预算：{v}（影响技术选型和美术风格）",
    "timeline": lambda v: f"开发周期：{v}（决定功能范围和迭代计划）",
}


def parse_constraints(constraint_str: str) -> str:
    """
    解析约束条件字符串，转换为 LLM 能理解的格式
    
    Args:
        constraint_str: 如 "platform:移动端，scope:独立小品"
        
    Returns:
        格式化后的约束条件描述
    """
    if not constraint_str or constraint_str.strip() == "":
        return "无特殊约束"
    
    constraints = []
    parts = constraint_str.replace("，", ",").split(",")
    
    for part in parts:
        if ":" in part:
            key, value = part.split(":", 1)
            key = key.strip().lower()
            value = value.strip()
            
            if key in CONSTRAINT_TEMPLATES:
                constraints.append(CONSTRAINT_TEMPLATES[key](value))
            else:
                constraints.append(f"{key}: {value}")
        else:
            constraints.append(part)
    
    return "\n".join(constraints) if constraints else "无特殊约束"
